def normalize_symbol(op: str) -> str:
    if not op:
        return ""
    first = op.split(",", 1)[0].strip()
    first = first.replace("BANKMASK(", "").replace(")", "")
    return first.lstrip("#").strip()

def target_label(op: str) -> str:
    return normalize_symbol(op)

def label_map(lines: List[AsmLine]) -> Dict[str, int]:
    return {ln.label: i for i, ln in enumerate(lines) if ln.label}

def next_code(lines: List[AsmLine], i: int) -> Optional[int]:
    for j in range(i + 1, len(lines)):
        if lines[j].mnemonic or lines[j].label:
            return j
    return None

def prev_code(lines: List[AsmLine], i: int) -> Optional[int]:
    for j in range(i - 1, -1, -1):
        if lines[j].mnemonic or lines[j].label:
            return j
    return None

def line_window(lines: List[AsmLine], i: int, radius: int = 3) -> List[str]:
    lo = max(0, i - radius)
    hi = min(len(lines), i + radius + 1)
    rows = []
    for k in range(lo, hi):
        ln = lines[k]
        if ln.mnemonic or ln.label:
            pointer = "=>" if k == i else "  "
            rows.append(f"{pointer} {ln.physical_line}: {ln.asm_text or ln.raw.strip()}")
    return rows

def reg_display(name_or_num: str, sfr_by_addr: Dict[int, List[str]]) -> str:
    x = name_or_num.strip()
    if re.fullmatch(r"\d+", x):
        n = int(x)
        names = sfr_by_addr.get(n)
        if names:
            return f"{x} ({'/'.join(names[:3])})"
    return x

def operand_display_for(mnemonic: Optional[str], operands: str, sfr_by_addr: Dict[int, List[str]]) -> str:
    parts = [p.strip() for p in operands.split(",")]
    if mnemonic in FILE_REGISTER_OPS and parts:
        parts[0] = reg_display(parts[0], sfr_by_addr)
    return ", ".join(parts)

class Analyzer:
    def __init__(self, lines: List[AsmLine], focus: str = "program"):
        self.lines = lines
        self.focus = focus
        self.labels = label_map(lines)
        self.findings: List[Finding] = []
        self.sfr_by_addr, self.addr_by_sfr = self.extract_symbols()
        self.program_indices = self.compute_program_indices()

    def extract_symbols(self) -> Tuple[Dict[int, List[str]], Dict[str, int]]:
        by_addr: Dict[int, List[str]] = defaultdict(list)
        by_name: Dict[str, int] = {}
        for ln in self.lines:
            text = ln.asm_text or ln.raw
            m = re.match(r"\s*([A-Za-z_][\w$]*)\s+(equ|set)\s+([0-9A-Fa-fx]+|\d+)\b", text, re.I)
            if m:
                name = m.group(1)
                val_s = m.group(3)
                try:
                    val = int(val_s, 0) if val_s.lower().startswith("0x") else int(val_s)
                except ValueError:
                    try:
                        val = int(val_s, 16)
                    except ValueError:
                        continue
                if len(by_addr[val]) < 12:
                    by_addr[val].append(name)
                by_name[name] = val
        return dict(by_addr), by_name

    def compute_program_indices(self) -> List[int]:
        idx = []
        in_maintext = False
        for i, ln in enumerate(self.lines):
            if ln.mnemonic == "psect":
                ps = ln.operands.split(",", 1)[0].strip().lower()
                in_maintext = ps in {"maintext", "text", "reset_vec", "cinit"} or "text" in ps
            if self.focus == "all":
                if ln.mnemonic or ln.label:
                    idx.append(i)
            elif self.focus == "maintext":
                if in_maintext and (ln.mnemonic or ln.label):
                    idx.append(i)
            else:
                if (ln.mnemonic or ln.label) and (in_maintext or ln.mnemonic not in {"equ", "set"}):
                    idx.append(i)
        return idx

    def add(self, sev: str, cat: str, i: int, msg: str, why: str, sug: str, related: Optional[List[int]] = None):
        ln = self.lines[i]
        parsed = ln.asm_text or ln.code or ln.raw.strip()
        if ln.mnemonic and ln.operands:
            parsed = f"{ln.mnemonic} {operand_display_for(ln.mnemonic, ln.operands, self.sfr_by_addr)}"
        self.findings.append(Finding(
            severity=sev, category=cat, line=ln.physical_line, message=msg, why=why,
            suggestion=sug, raw=ln.raw, parsed=parsed, source_ref=ln.source_ref,
            address=ln.address_text, related_lines=related or [], context=line_window(self.lines, i)
        ))

    def analyze(self) -> List[Finding]:
        self.find_nops()
        self.find_banks()
        self.find_branches()
        self.find_delay_loops()
        self.find_movlw_patterns()
        self.find_status_tests()
        self.find_call_patterns()
        return sorted(self.findings, key=lambda f: (f.line, f.category))

    def consider(self, i: int) -> bool:
        return i in self.program_indices

    def find_nops(self):
        for i, ln in enumerate(self.lines):
            if ln.mnemonic != "nop" or not self.consider(i):
                continue
            p = prev_code(self.lines, i)
            n = next_code(self.lines, i)
            pm = self.lines[p].mnemonic if p is not None else None
            nm = self.lines[n].mnemonic if n is not None else None
            if pm in SKIPS:
                self.add("INFO", "NOP_AFTER_SKIP", i, "NOP follows a skip-class instruction.", "A NOP after DECFSZ/BTFSS/etc. is often deliberate single-instruction padding.", "Usually keep. It may be part of exact timing or skip balancing.", [self.lines[p].physical_line] if p else [])
            elif pm in BRANCHES or pm in RETURNS:
                self.add("WARN", "NOP_AFTER_CONTROL_TRANSFER", i, "NOP follows an unconditional control transfer.", "Normal execution usually cannot fall through to this NOP.", "Inspect whether this is padding, delay-loop residue, or a branch target. Remove only if not timing-relevant.", [self.lines[p].physical_line] if p else [])
            elif nm in RETURNS:
                self.add("WARN", "NOP_BEFORE_RETURN", i, "NOP immediately precedes a return.", "This usually costs one cycle unless deliberately inserted.", "Check timing intent. If not needed, remove at source/asm level.", [self.lines[n].physical_line] if n else [])
            else:
                self.add("INFO", "NOP_GENERAL", i, "NOP instruction found.", "May be timing padding, alignment, or compiler residue.", "Review in context; do not remove blindly.")

    def find_banks(self):
        last: Optional[Tuple[str, str, int]] = None
        for i, ln in enumerate(self.lines):
            if not self.consider(i) or not ln.mnemonic:
                continue
            if ln.label or ln.mnemonic in BARRIERS:
                last = None  # Clear context state across sequence junctions
                if ln.mnemonic in BARRIERS:
                    continue
            if ln.mnemonic in BANK_SELECTS:
                op = normalize_symbol(ln.operands)
                if last and last[0] == ln.mnemonic and last[1] == op:
                    self.add("WARN", "REPEATED_BANK_SELECT", i, f"Repeated {ln.mnemonic.upper()} {op}.", "Repeated bank selects can be compiler conservatism, but are sometimes required after uncertain control flow.", "If no label/call/branch can enter between the two, the later bank select may be removable.", [self.lines[last[2]].physical_line])
                last = (ln.mnemonic, op, i)

    def find_branches(self):
        for i, ln in enumerate(self.lines):
            if not self.consider(i) or ln.mnemonic not in BRANCHES:
                continue
            tgt = target_label(ln.operands)
            if tgt and tgt in self.labels:
                ni = next_code(self.lines, i)
                if ni is not None and self.lines[ni].label == tgt:
                    self.add("STRONG", "BRANCH_TO_NEXT_LABEL", i, f"{ln.mnemonic.upper()} goes to the next label {tgt}.", "A branch to the very next executable location usually wastes cycles and code space.", "Remove only if not needed for psect/debug/linker artifact.", [self.lines[ni].physical_line])
                if self.labels[tgt] == i or self.lines[i].label == tgt:
                    self.add("WARN", "SELF_BRANCH", i, f"{ln.mnemonic.upper()} appears to branch to itself.", "This is an idle/error trap pattern or a bug.", "Keep for deliberate halt loops only.", [self.lines[self.labels[tgt]].physical_line])
            p = prev_code(self.lines, i)
            if p is not None and self.lines[p].mnemonic in SKIPS:
                n = next_code(self.lines, i)
                if n is not None and self.lines[n].mnemonic in BRANCHES:
                    self.add("INFO", "SKIP_WITH_DOUBLE_GOTO", p, "Skip instruction feeds a two-GOTO decision pattern.", "XC8 often implements if/else as skip + goto + goto. Correct, but not always compact.", "For tiny code, inspect whether source logic can be rearranged so the fall-through path is the common path.", [self.lines[i].physical_line, self.lines[n].physical_line])

    def find_delay_loops(self):
        for i, ln in enumerate(self.lines):
            if not self.consider(i) or not ln.label:
                continue
            window = []
            j = i
            for _ in range(10):
                j = next_code(self.lines, j)
                if j is None:
                    break
                window.append(j)
            if not window:
                continue
            mn = [self.lines[k].mnemonic for k in window]
            jumps_back = [k for k in window if self.lines[k].mnemonic in BRANCHES and target_label(self.lines[k].operands) == ln.label]
            if any(m in {"decfsz", "incfsz"} for m in mn) and jumps_back:
                self.add("INFO", "DELAY_LOOP", i, f"Label '{ln.label}' appears to start a delay/timing loop.", "Counter/skip/branch loop detected. This is commonly generated for __delay_ms/us style delays.", "If timing is not critical, consider a hardware timer or cooperative wait. If timing is critical, keep it but document the oscillator assumption.", [self.lines[k].physical_line for k in window[:8]])

    def find_movlw_patterns(self):
        last: Optional[Tuple[str, int]] = None
        for i, ln in enumerate(self.lines):
            if not self.consider(i) or not ln.mnemonic:
                continue
            if ln.label or ln.mnemonic in BARRIERS:
                last = None
            if ln.mnemonic == "movlw":
                val = ln.operands.strip()
                if last and last[0] == val:
                    self.add("WARN", "REPEATED_MOVLW", i, f"MOVLW reloads literal {val} already loaded earlier.", "Reloading W with the same literal may be redundant if W was not consumed/clobbered.", "Inspect intervening instructions. If W was still valid, source may be forcing repeated loads.", [self.lines[last[1]].physical_line])
                last = (val, i)
            elif ln.mnemonic in {"addlw", "andlw", "iorlw", "xorlw", "sublw", "movf"}:
                last = None

    def find_status_tests(self):
        for i, ln in enumerate(self.lines):
            if not self.consider(i) or ln.mnemonic not in {"btfss", "btfsc"}:
                continue
            ops = [p.strip() for p in ln.operands.split(",")]
            if len(ops) >= 2 and ops[0] in {"3", "STATUS", "status"}:
                if ops[1] == "2":
                    self.add("INFO", "STATUS_Z_TEST", i, "Code tests STATUS,Z after arithmetic/logical work.", "This is the normal PIC way to implement comparisons such as == 0.", "Useful marker: inspect the previous one or two instructions to understand the C condition being implemented.")

    def find_call_patterns(self):
        calls = []
        current = None
        graph = defaultdict(list)
        line_of = {}
        for i, ln in enumerate(self.lines):
            if not self.consider(i):
                continue
            if ln.label:
                current = ln.label
                line_of[current] = ln.physical_line
            if ln.mnemonic in CALLS:
                tgt = target_label(ln.operands)
                calls.append((i, tgt))
                if current and tgt:
                    graph[current].append(tgt)
        counts = Counter(t for _, t in calls if t)
        for tgt, c in counts.items():
            if c >= 5:
                ls = [self.lines[idx].physical_line for idx, t in calls if t == tgt][:10]
                self.add("INFO", "HOT_CALL_TARGET", calls[0][0], f"Function '{tgt}' is called {c} times.", "Frequent calls to tiny functions may cost more than the work they perform.", "Inspect whether this helper should be inline or simplified.", ls)
        for a, tgts in graph.items():
            if a in tgts:
                for idx, t in calls:
                    if t == a:
                        self.add("WARN", "SELF_CALL", idx, f"Function/label '{a}' appears to call itself.", "PIC hardware return stacks are shallow; recursion is usually dangerous.", "Verify this is not accidental recursion.", [line_of.get(a, 0)])

