#!/usr/bin/env python3
"""
xc8_asm_revelator_v4_0.py

Smarter conservative XC8/PIC assembly/listing review tool, v4.0.

Reads Microchip XC8 / PIC-AS .lst, .s, or .asm files and produces a programmer-focused
Markdown report, JSON report, and optionally an annotated listing. It does not blindly
rewrite code. It explains what the emitted assembly appears to do and where a human
should inspect possible inefficiency.

Python 3.9+, no third-party dependencies.
"""
from __future__ import annotations

import argparse
import dataclasses
import json
import re
from collections import Counter, defaultdict
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any

KNOWN_MNEMONICS = {
    # enhanced midrange PIC instruction set + common pseudo/directives
    "addlw","addwf","andlw","andwf","asrf","bcf","bra","brw","bsf","btfsc","btfss",
    "call","clrc","clrf","clrw","clrwdt","comf","cpfseq","cpfsgt","cpfslt","decf","decfsz",
    "goto","incf","incfsz","iorlw","iorwf","lslf","lsrf","movf","movlb","movlp","movlw",
    "movwf","nop","option","pagesel","psect","reset","retfie","retlw","return","rlf","rrf",
    "setc","sleep","sublw","subwf","swapf","tris","xorlw","xorwf","ljmp","fcall",
    # directives / pseudo ops
    "processor","pagewidth","opt","dabs","equ","set","global","extrn","org","end","dw","db","de",
    "config","radix","space","align","class","section",
}
BRANCHES = {"goto", "bra", "ljmp"}
CALLS = {"call", "fcall"}
RETURNS = {"return", "retlw", "retfie"}
SKIPS = {"btfsc", "btfss", "decfsz", "incfsz", "cpfseq", "cpfsgt", "cpfslt"}
BANK_SELECTS = {"movlb", "banksel"}
BARRIERS = BRANCHES | CALLS | RETURNS | {"psect", "org", "end", "global", "section"}
DIRECTIVES = {"processor","pagewidth","opt","dabs","equ","set","global","extrn","org","end","dw","db","de","config","radix","space","align","class","section","psect"}
FILE_REGISTER_OPS = {
    "clrf","movwf","movf","bcf","bsf","rlf","rrf","swapf","andwf","iorwf","xorwf",
    "addwf","subwf","decf","incf","decfsz","incfsz","btfsc","btfss","cpfseq","cpfsgt","cpfslt","lslf","lsrf","asrf"
}

def rough_cycles(mnemonic: Optional[str], operands: str = "") -> Tuple[float, str]:
    if not mnemonic:
        return 0.0, "not an instruction"
    m = mnemonic.lower()
    if m in DIRECTIVES:
        return 0.0, "directive/pseudo-op"
    if m in BRANCHES or m in CALLS or m in RETURNS:
        return 2.0, "control transfer, usually 2 cycles on midrange PICs"
    if m in SKIPS:
        return 1.5, "skip-class instruction; cost depends on whether the skip is taken"
    return 1.0, "ordinary single-cycle instruction estimate"

@dataclasses.dataclass
class AsmLine:
    physical_line: int
    raw: str
    list_line: Optional[int] = None
    address: Optional[int] = None
    address_text: str = ""
    opcode_text: str = ""
    asm_text: str = ""
    code: str = ""
    comment: str = ""
    label: Optional[str] = None
    mnemonic: Optional[str] = None
    operands: str = ""
    psect: Optional[str] = None
    source_ref: Optional[str] = None

    def is_instruction(self) -> bool:
        return self.mnemonic is not None and self.mnemonic not in DIRECTIVES

@dataclasses.dataclass
class Finding:
    severity: str
    category: str
    line: int
    message: str
    why: str
    suggestion: str
    raw: str
    parsed: str = ""
    source_ref: Optional[str] = None
    address: str = ""
    related_lines: List[int] = dataclasses.field(default_factory=list)
    context: List[str] = dataclasses.field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return dataclasses.asdict(self)

