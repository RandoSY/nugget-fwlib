def split_comment(s: str) -> Tuple[str, str]:
    if ";" in s:
        a, b = s.split(";", 1)
        return a.rstrip(), ";" + b.rstrip()
    return s.rstrip(), ""

def hex_to_int(s: str) -> Optional[int]:
    try:
        return int(s, 16)
    except ValueError:
        return None

def parse_line(n: int, raw: str, current_psect: Optional[str], last_source_ref: Optional[str]) -> AsmLine:
    line = raw.rstrip("\n")
    out = AsmLine(physical_line=n, raw=line, psect=current_psect, source_ref=last_source_ref)
    s = line.rstrip()

    if not s.strip():
        return out

    # Capture source reference lines generated by compiler
    msrc = re.search(r";\s*([^;]*\.(?:c|h|s|asm)):\s*(\d+)\s*:\s*(.*)$", s, re.I)
    if msrc:
        out.comment = s.strip()
        out.source_ref = f"{msrc.group(1).strip()}:{msrc.group(2)}: {msrc.group(3).strip()}"
        return out

    # Refined Listing Separator Pipeline
    m = re.match(r"^\s*(\d+)\s+([0-9A-Fa-f]{4,6})?\s*((?:[0-9A-Fa-f]{4}(?:\s+[0-9A-Fa-f]{4})*))?\s*(.*)$", s)
    candidate = s.strip()
    if m:
        out.list_line = int(m.group(1))
        if m.group(2):
            out.address_text = m.group(2).upper()
            out.address = hex_to_int(m.group(2))
        if m.group(3):
            out.opcode_text = " ".join(m.group(3).split())
        candidate = (m.group(4) or "").strip()

    if not candidate or candidate.startswith("!"):
        if candidate.startswith("!"):
            out.comment = candidate
        return out

    code, comment = split_comment(candidate)
    out.asm_text = candidate
    out.code = code
    out.comment = comment
    stripped = code.strip()
    if not stripped:
        return out

    # Handle Labels with explicit trailing match protections
    lm = re.match(r"^([A-Za-z_.$?][\w.$?@#]*):\s*(.*)$", stripped)
    rest = stripped
    if lm:
        out.label = lm.group(1)
        rest = lm.group(2).strip()

    if rest:
        parts = rest.split(None, 1)
        tok = parts[0].lower()
        if tok in KNOWN_MNEMONICS:
            out.mnemonic = tok
            out.operands = parts[1].strip() if len(parts) > 1 else ""
            if tok == "psect":
                out.psect = out.operands.split(",", 1)[0].strip()
    return out

def parse_file(path: Path) -> List[AsmLine]:
    raw_lines = path.read_text(errors="replace").splitlines()
    out: List[AsmLine] = []
    current_psect: Optional[str] = None
    last_source_ref: Optional[str] = None
    for i, raw in enumerate(raw_lines, 1):
        ln = parse_line(i, raw, current_psect, last_source_ref)
        if ln.source_ref and not (ln.mnemonic or ln.label):
            last_source_ref = ln.source_ref
        if ln.mnemonic == "psect":
            current_psect = ln.psect
        else:
            ln.psect = current_psect
        if ln.mnemonic or ln.label:
            ln.source_ref = last_source_ref
        out.append(ln)
    return out

